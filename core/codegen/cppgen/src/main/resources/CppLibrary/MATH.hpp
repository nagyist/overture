#ifndef INCLUDE_MATH_HPP_
#define INCLUDE_MATH_HPP_

namespace MATH
{

const double pi = M_PI;

double atan(const double v)
{
	return ::atan(v);
}

double cos(const double v)
{
	return ::cos(v);
}

double sin(const double v)
{
	return ::sin(v);
}

double sqrt(const double v)
{
	return ::sqrt(v);
}


}



#endif /* INCLUDE_MATH_HPP_ */
